
public class d3p4 {

	public static void main(String[] args) {
		//widening
		int var1=2;
		double var2=var1;
		System.out.println(var2);
		//narrowing
		double var3=2.56;
		int var4=(int)var3;
		System.out.println(var4);
		
		//char to int
		char var5='A';
		int var6=(int)var5;
		System.out.println(var6);
		// int to char
		int var7=97;
		char var8=(char)var7;
		System.out.println(var8);

	}

}
